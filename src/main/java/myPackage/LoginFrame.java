    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
        if(username.toString().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Username Cannot Be Empty.");
            txtUsername.setText("");
            txtPassword.setText("");
        }
        else if(username.toString().equalsIgnoreCase("admin") && password.toString().equals("admin"))
        {
            txtUsername.setText("");
            txtPassword.setText("");
            JOptionPane.showMessageDialog(this, "Login As Admin.");
            Menu menu = new Menu();
            menu.setVisible(true);
            this.hide();
        }
        else if(username.toString().equalsIgnoreCase("Guest"))
        {
            JOptionPane.showMessageDialog(this, "Login As Guest.");
            Menu menu = new Menu(1);
            menu.setVisible(true);
            this.hide();
           
        }
        else 
        {
            JOptionPane.showMessageDialog(this, "Username Or Password Doesn't Match");
            txtUsername.setText("");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    
